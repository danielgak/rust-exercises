pub fn num_hits_x(curr: (i32, i32), max: (i32, i32), soup: &Vec<Vec<char>>) -> i32 {
    let mut hits = 0;

    if soup[curr.0 as usize][curr.1 as usize] != 'A' {
        return 0;
    }

    for dir in vec![
        XDirection::UpRight,
        XDirection::DownRight,
        XDirection::DownLeft,
        XDirection::UpLeft,
    ] {
        let diagonal = retrieve_diagonal_checked(&dir, curr, max, &soup);
        match diagonal {
            Some(vec) => {
                let str = vec.iter().collect::<String>();
                if "MAS" == str.as_str() {
                    hits += 1;
                }
            }
            None => continue,
        }
    }

    if hits > 1 {
        return 1;
    }

    0
}

pub fn retrieve_diagonal_checked(
    d: &XDirection,
    curr: (i32, i32),
    max: (i32, i32),
    soup: &Vec<Vec<char>>,
) -> Option<Vec<char>> {
    let mut x = curr.0;
    let mut y = curr.1;

    let vec = match d {
        XDirection::UpRight => vec![
            retrieve_value_checked(soup, (x + 1, y + 1), max),
            Some('A'),
            retrieve_value_checked(soup, (x - 1, y - 1), max),
        ],
        XDirection::DownRight => vec![
            retrieve_value_checked(soup, (x + 1, y - 1), max),
            Some('A'),
            retrieve_value_checked(soup, (x - 1, y + 1), max),
        ],
        XDirection::DownLeft => vec![
            retrieve_value_checked(soup, (x - 1, y - 1), max),
            Some('A'),
            retrieve_value_checked(soup, (x + 1, y + 1), max),
        ],
        XDirection::UpLeft => vec![
            retrieve_value_checked(soup, (x - 1, y + 1), max),
            Some('A'),
            retrieve_value_checked(soup, (x + 1, y - 1), max),
        ],
    };

    match vec[..] {
        [Some(v0), Some(v1), Some(v2)] => Some(vec![v0, v1, v2]),
        [..] => None,
    }
}

pub fn retrieve_value_checked(
    soup: &Vec<Vec<char>>,
    at: (i32, i32),
    max: (i32, i32),
) -> Option<char> {
    if at.0 >= max.0 || at.1 >= max.1 || at.0 < 0 || at.1 < 0 {
        return None;
    }

    Some(soup[at.0 as usize][at.1 as usize])
}

