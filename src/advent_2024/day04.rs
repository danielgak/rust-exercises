pub fn num_hits(curr: (i32, i32), max: (i32, i32), soup: &Vec<Vec<char>>) -> i32 {
    let mut hits = 0;

    for dir in vec![
        SearchDirection::Up,
        SearchDirection::UpRight,
        SearchDirection::Right,
        SearchDirection::DownRight,
        SearchDirection::Down,
        SearchDirection::DownLeft,
        SearchDirection::Left,
        SearchDirection::UpLeft,
    ] {
        let mut vec = Vec::new();
        for step in vec![0, 1, 2, 3] {
            let char = retrieve_character_checked(&dir, curr, max, step, &soup);
            match char {
                Some(v) => vec.push(v),
                None => break,
            }
        }

        let str = vec.iter().collect::<String>();
        if "XMAS" == str.as_str() {
            hits += 1;
        }
    }

    hits
}

pub fn retrieve_character_checked(
    d: &SearchDirection,
    curr: (i32, i32),
    max: (i32, i32),
    steps: i32,
    soup: &Vec<Vec<char>>,
) -> Option<char> {
    let mut new_x = curr.0;
    let mut new_y = curr.1;
    if steps == 0 {
        return Some(soup[new_y as usize][new_x as usize]);
    }

    match d {
        SearchDirection::Up => new_y += steps,
        SearchDirection::UpRight => {
            new_y += steps;
            new_x += steps
        }
        SearchDirection::Right => new_x += steps,
        SearchDirection::DownRight => {
            new_y -= steps;
            new_x += steps
        }
        SearchDirection::Down => new_y -= steps,
        SearchDirection::DownLeft => {
            new_y -= steps;
            new_x -= steps
        }
        SearchDirection::Left => new_x -= steps,
        SearchDirection::UpLeft => {
            new_y += steps;
            new_x -= steps
        }
    }

    if new_x >= max.0 || new_y >= max.1 || new_x < 0 || new_y < 0 {
        return None;
    }

    Some(soup[new_y as usize][new_x as usize])
}

pub fn parse_into_vectors(str: &str) -> Vec<Vec<char>> {
    let mut result = Vec::new();
    let mut current = Vec::new();
    for c in str.chars() {
        if c == '\n' {
            result.push(current);
            current = Vec::new();
        } else {
            current.push(c);
        }
    }
    result.push(current);
    result
}

